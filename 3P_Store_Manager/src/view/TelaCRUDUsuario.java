    private boolean validadorCamposObrigatorios() {
        boolean retorno = false;
        if (!textoNome.getText().trim().equals("") && !textoLogin.getText().trim().equals("")
                && !textoSenha.getText().trim().equals("") && !textoConfirmarSenha.getText().trim().equals("")) {
            retorno = true;
        }
        return retorno;
    }

    private boolean validarSenha() {
        boolean retorno = false;
        if (textoSenha.getText().equals(textoConfirmarSenha.getText())) {
            retorno = true;
        }
        return retorno;
    }

    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed

        if (validadorCamposObrigatorios()) {
            if (status == 1) {
                if (validarSenha()) {
                    Usuario usuario = new Usuario(textoNome.getText(), textoLogin.getText(), textoSenha.getText(), (Cargo) comboCargo.getSelectedItem());
                    ctr.getCtrUsuarios().inserir(usuario);
                    limparCampos();
                } else {
                    JOptionPane.showMessageDialog(null, "Senhas diferentes");
                }
            } else if (status == 2) {
                if (validarSenha()) {
                    usuarioSelecionado.setNome(textoNome.getText());
                    usuarioSelecionado.setLogin(textoLogin.getText());
                    usuarioSelecionado.setCargo((Cargo) comboCargo.getSelectedItem());
                    usuarioSelecionado.setSenha(textoSenha.getText());
                    ctr.getCtrUsuarios().atualizar(usuarioSelecionado);
                } else {
                    JOptionPane.showMessageDialog(null, "Senhas diferentes");
                }
            }
            preencherTabela();

        } else {
            JOptionPane.showMessageDialog(null, "Preencher todos os campos obrigatorios");
        }
    }//GEN-LAST:event_botaoSalvarActionPerformed

    private void limparCampos() {
        textoNome.setText("");
        textoLogin.setText("");
        textoSenha.setText("");
        textoConfirmarSenha.setText("");
        comboCargo.setSelectedIndex(0);
    }


    private void botaoLimparActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoLimparActionPerformed
        limparCampos();
    }//GEN-LAST:event_botaoLimparActionPerformed

    private void botaoNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoNovoActionPerformed
        status = 1;
        limparCampos();
    }//GEN-LAST:event_botaoNovoActionPerformed

    private void botaoEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoEditarActionPerformed

        status = 2;
        int selecionado = tabela.getSelectedRow();
        if (selecionado < 0) {
            JOptionPane.showMessageDialog(null, "Usuario não selecionado");
        } else if (status == 2) {
            usuarioSelecionado = ctr.getCtrUsuarios().pesquisarPorCodigo((int) tabela.getValueAt(selecionado, 0));
            if (usuarioSelecionado != null) {
                textoNome.setText(usuarioSelecionado.getNome());
                textoLogin.setText(usuarioSelecionado.getLogin());
                comboCargo.setSelectedItem(usuarioSelecionado.getCargo());

            }
        }
    }//GEN-LAST:event_botaoEditarActionPerformed

    private void botaoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoExcluirActionPerformed
        int selecionado = tabela.getSelectedRow();
        if (selecionado < 0) {
            JOptionPane.showMessageDialog(null, "Usuario não selecionada");

        } else {
            ctr.getCtrUsuarios().excluir((int) tabela.getValueAt(selecionado, 0));
            preencherTabela();
        }
    }//GEN-LAST:event_botaoExcluirActionPerformed


