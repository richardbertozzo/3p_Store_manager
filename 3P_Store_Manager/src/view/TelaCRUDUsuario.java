    private void preencherTabela() {

        modelo.setNumRows(0);
        List<Usuario> lista = ctr.getCtrUsuarios().listarUsuarios();

        for (Usuario u : lista) {
            modelo.addRow(new Object[]{u.getCodigo(), u.getNome(), u.getLogin(), u.getCargo()});
        }
        tabela.setModel(modelo);
    }

