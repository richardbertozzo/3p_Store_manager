    private boolean validadorCamposObrigatorios() {
        boolean retorno = false;
        if (!textoNome.getText().trim().equals("") && !textoPrecoCusto.getText().trim().equals("")
                && !textoPrecoVenda.getText().trim().equals("") && !textoQM.getText().trim().equals("")) {
            retorno = true;
        }
        return retorno;
    }

    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
        if (validadorCamposObrigatorios()) {
            if (comboBoxQuantidade.getSelectedIndex() >= Integer.parseInt(textoQM.getText())) {
                if (status == 1) {

                    Camiseta camiseta = new Camiseta(textoNome.getText().trim(), textoTag.getText().trim(), Double.parseDouble(textoPrecoCusto.getText().trim()),
                            Double.parseDouble(textoPrecoVenda.getText().trim()), Integer.parseInt(textoQM.getText().trim()),
                            comboBoxQuantidade.getSelectedIndex(), (Tamanho) comboBoxTamanho.getSelectedItem());
                    ctr.getCtrCamisetas().inserir(camiseta);

                } else if (status == 2) {
                    camisetaSelecionada.setNome(textoNome.getText().trim());
                    camisetaSelecionada.setHashTag(textoTag.getText().trim());
                    camisetaSelecionada.setPrecoCusto(Double.parseDouble(textoPrecoCusto.getText().trim()));
                    camisetaSelecionada.setPrecoVenda(Double.parseDouble(textoPrecoVenda.getText().trim()));
                    camisetaSelecionada.setQm(Integer.parseInt(textoQM.getText().trim()));
                    camisetaSelecionada.setQuantidade(comboBoxQuantidade.getSelectedIndex());
                    camisetaSelecionada.setTamanho((Tamanho) comboBoxTamanho.getSelectedItem());
                    ctr.getCtrCamisetas().atualizar(camisetaSelecionada);
                    limparCampos();
                }
                preencherTabela();
            } else {
                JOptionPane.showMessageDialog(null, "Quantidade em estoque deve ser maior que QM");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Preencher todos os campos obrigatorios!");
        }
    }//GEN-LAST:event_botaoSalvarActionPerformed

    private void limparCampos() {
        textoNome.setText("");
        textoTag.setText("");
        textoPrecoCusto.setText("");
        textoPrecoVenda.setText("");
        textoQM.setText("");
        comboBoxTamanho.setSelectedIndex(0);
        comboBoxQuantidade.setSelectedIndex(0);
    }

