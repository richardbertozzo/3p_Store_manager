    private void botaoEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoEditarActionPerformed

        setStatus(2);
        int selecionado = tabela.getSelectedRow();
        if (selecionado >= 0) {
            camisetaSelecionada = ctr.getCtrCamisetas().pesquisarPorCodigo((int) tabela.getValueAt(selecionado, 0));
            if (camisetaSelecionada != null) {
                textoNome.setText(camisetaSelecionada.getNome().trim());
                textoTag.setText(camisetaSelecionada.getHashTag().trim());
                textoPrecoCusto.setText(Double.toString(camisetaSelecionada.getPrecoCusto()));
                textoPrecoVenda.setText(Double.toString(camisetaSelecionada.getPrecoVenda()));
                textoQM.setText(Integer.toString(camisetaSelecionada.getQm()));
                comboBoxQuantidade.setSelectedIndex(camisetaSelecionada.getQuantidade());
                comboBoxTamanho.setSelectedItem(camisetaSelecionada.getTamanho());
            } else {
                JOptionPane.showMessageDialog(null, "Camiseta não selecionada");
            }
        }
    }//GEN-LAST:event_botaoEditarActionPerformed

    private void botaoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoExcluirActionPerformed
        int selecionado = tabela.getSelectedRow();
        if (selecionado >= 0) {
            ctr.getCtrCamisetas().excluir((int) tabela.getValueAt(selecionado, 0));
            preencherTabela();

        } else {
            JOptionPane.showMessageDialog(null, "Camiseta não selecionada");
        }
    }//GEN-LAST:event_botaoExcluirActionPerformed

